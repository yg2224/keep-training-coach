PLAN_TYPE_TRANSLATION_KEYS = {
    "rolling_week": "plans.rolling_week",
    "race_goal": "plans.race_goal",
}


def normalize_language(value):
    normalized = (value or "").strip().lower()
    if normalized in SUPPORTED_LANGUAGES:
        return normalized
    return None


def translate(lang, key, **kwargs):
    current = TRANSLATIONS.get(lang, TRANSLATIONS[DEFAULT_LANGUAGE])
    template = current.get(key) or TRANSLATIONS[DEFAULT_LANGUAGE].get(key) or key
    if kwargs:
        return template.format(**kwargs)
    return template


def get_calendar_weekday_labels(lang):
    return CALENDAR_WEEKDAY_LABELS.get(lang, CALENDAR_WEEKDAY_LABELS[DEFAULT_LANGUAGE])


def format_calendar_month_label(month_start, lang):
    if lang == "zh":
        return f"{month_start.year} 年 {month_start.month} 月"
    return month_start.strftime("%B %Y")


def translate_plan_type(lang, value):
    key = PLAN_TYPE_TRANSLATION_KEYS.get(value)
    if not key:
        return value or ""
    return translate(lang, key)


def translate_status(lang, value):
    if not value:
        return ""
    return translate(lang, f"status.{value}")
